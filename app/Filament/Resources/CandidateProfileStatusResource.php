<?php

namespace App\Filament\Resources;

use App\Filament\Resources\CandidateProfileStatusResource\Pages;
use App\Filament\Resources\CandidateProfileStatusResource\RelationManagers;
use App\Models\CandidateProfileStatus;
use Filament\Forms;
use Filament\Resources\Form;
use Filament\Resources\Resource;
use Filament\Resources\Table;
use Filament\Tables;
use Illuminate\Database\Eloquent\Builder;
use Illuminate\Database\Eloquent\SoftDeletingScope;

class CandidateProfileStatusResource extends Resource
{
    protected static ?string $model = CandidateProfileStatus::class;

    protected static ?string $navigationIcon = 'heroicon-o-view-boards';

    protected static ?string $activeNavigationIcon = 'heroicon-s-view-boards';

    protected static ?string $navigationGroup = 'Form Components';

    public static function form(Form $form): Form
    {
        return $form
            ->schema([
                Forms\Components\TextInput::make('status')
                    ->required()
                    ->maxLength(255),
            ]);
    }

    public static function table(Table $table): Table
    {
        return $table
            ->columns([
                //
                Tables\Columns\TextColumn::make('status')
                    ->sortable()
                    ->searchable(),
            ])
            ->filters([
                Tables\Filters\TrashedFilter::make(),
            ])
            ->actions([
                Tables\Actions\EditAction::make(),
            ])
            ->bulkActions([
                Tables\Actions\DeleteBulkAction::make(),
                Tables\Actions\ForceDeleteBulkAction::make(),
                Tables\Actions\RestoreBulkAction::make(),
            ]);
    }
    
    public static function getRelations(): array
    {
        return [
            //
        ];
    }
    
    public static function getPages(): array
    {
        return [
            'index' => Pages\ListCandidateProfileStatuses::route('/'),
            'create' => Pages\CreateCandidateProfileStatus::route('/create'),
            'edit' => Pages\EditCandidateProfileStatus::route('/{record}/edit'),
        ];
    }    
    
    public static function getEloquentQuery(): Builder
    {
        return parent::getEloquentQuery()
            ->withoutGlobalScopes([
                SoftDeletingScope::class,
            ]);
    }
}
